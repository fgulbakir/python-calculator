""" Simple Calculator  """

print("Enter first number :")
firstNumber = int(input()) 
print("Enter second number :")
secondNumber = int(input())

print(str(firstNumber) + " + " + str(secondNumber)+ " = " + str(firstNumber + secondNumber))
print(str(firstNumber) + " - " + str(secondNumber)+ " = " + str(firstNumber - secondNumber))
print(str(firstNumber) + " * " + str(secondNumber)+ " = " + str(firstNumber * secondNumber))
print(str(firstNumber) + " / " + str(secondNumber)+ " = " + str(firstNumber / secondNumber))


